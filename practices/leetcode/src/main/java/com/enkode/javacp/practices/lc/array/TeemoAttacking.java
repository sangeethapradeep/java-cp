  private static int findPoisonedDuration(int[] timeSeries, int duration) {
    int t = timeSeries[0];
    int totalTime = 0;

    int n = timeSeries.length;

    for (int i = 1; i < n; i++) {
      if (timeSeries[i] > t + duration - 1) {
        totalTime += duration;
      } else {
        totalTime += timeSeries[i] - t;
      }
      t = timeSeries[i];
    }

    return totalTime + duration;
  }
